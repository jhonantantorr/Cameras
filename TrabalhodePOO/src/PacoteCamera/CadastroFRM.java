    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        // salvar aqui
        System.out.println("Salvar Aqui");
        String nome = nomeDaCamera.getText();
        int tipodeCamera = listTipo.getSelectedIndex();
        String especificações = descricao.getText();
        boolean habilitado = habilatado.getVerifyInputWhenFocusTarget();
        int localdaCamera = listCamera.getSelectedIndex();

        Camera camera = new Camera(0, nome, tipodeCamera, especificações, habilitado, localdaCamera);

        CadastroControles controlador = new CadastroControles();
        if (controlador.CadastrarProduto(camera) == true) {
            JOptionPane.showMessageDialog(this, " Cadastrado com Sucesso !");
            this.dispose();
            telaAnterior.setEnabled(true);
        } else {
            System.out.println(camera.isHabilitado() + "\n" + camera.getEspecificações() + "\n" + camera.getNome() + "\n" + camera.getLocaldaCamera() + "\n" + camera.getTipodeCamera());
            JOptionPane.showMessageDialog(this, "Nao cadastrado. \n\nFaltando dados");
        }


    }//GEN-LAST:event_salvarActionPerformed

